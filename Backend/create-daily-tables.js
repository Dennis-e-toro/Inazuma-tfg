import "dotenv/config";
import pkg from 'pg';
import { createPgConfig } from './pg-config.js';
const { Pool } = pkg;

const pool = new Pool(createPgConfig());

async function main() {
  try {
    await pool.query(`
      CREATE TABLE IF NOT EXISTS intentos_diarios (
        id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
        usuario_id INTEGER NOT NULL REFERENCES usuarios(id) ON DELETE CASCADE,
        modo_juego_id SMALLINT NOT NULL REFERENCES modos_juego(id) ON DELETE RESTRICT,
        personaje_diario_id BIGINT REFERENCES personaje_diario(id) ON DELETE CASCADE,
        dia DATE NOT NULL,
        intentos_usados SMALLINT NOT NULL DEFAULT 0,
        pistas_usadas SMALLINT NOT NULL DEFAULT 0,
        completado BOOLEAN NOT NULL DEFAULT FALSE,
        acertado BOOLEAN NOT NULL DEFAULT FALSE,
        inicio TIMESTAMPTZ NOT NULL DEFAULT NOW(),
        fin TIMESTAMPTZ,
        tiempo_ms BIGINT,
        puntuacion INTEGER NOT NULL DEFAULT 0,
        creado_en TIMESTAMPTZ NOT NULL DEFAULT NOW(),
        actualizado_en TIMESTAMPTZ NOT NULL DEFAULT NOW(),
        UNIQUE (usuario_id, modo_juego_id, dia),
        CONSTRAINT intentos_diarios_intentos_chk CHECK (intentos_usados >= 0),
        CONSTRAINT intentos_diarios_pistas_chk CHECK (pistas_usadas >= 0),
        CONSTRAINT intentos_diarios_tiempo_chk CHECK (tiempo_ms IS NULL OR tiempo_ms >= 0)
      );
    `);

    await pool.query(`CREATE INDEX IF NOT EXISTS idx_intentos_diarios_usuario ON intentos_diarios (usuario_id)`);
    await pool.query(`CREATE INDEX IF NOT EXISTS idx_intentos_diarios_dia_modo ON intentos_diarios (dia, modo_juego_id)`);
    await pool.query(`CREATE INDEX IF NOT EXISTS idx_intentos_diarios_puntuacion ON intentos_diarios (dia, modo_juego_id, puntuacion DESC)`);
    await pool.query(`CREATE INDEX IF NOT EXISTS idx_intentos_diarios_tiempo ON intentos_diarios (dia, modo_juego_id, tiempo_ms ASC NULLS LAST)`);

    await pool.query(`
      CREATE TABLE IF NOT EXISTS adivinanzas_diarias (
        id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
        intento_diario_id BIGINT NOT NULL REFERENCES intentos_diarios(id) ON DELETE CASCADE,
        personaje_adivinado_id BIGINT NOT NULL REFERENCES personajes(id) ON DELETE RESTRICT,
        es_correcta BOOLEAN NOT NULL DEFAULT FALSE,
        creado_en TIMESTAMPTZ NOT NULL DEFAULT NOW()
      );
    `);

    await pool.query(`CREATE INDEX IF NOT EXISTS idx_adivinanzas_intento ON adivinanzas_diarias (intento_diario_id)`);
    await pool.query(`CREATE INDEX IF NOT EXISTS idx_adivinanzas_personaje ON adivinanzas_diarias (personaje_adivinado_id)`);
    await pool.query(`CREATE INDEX IF NOT EXISTS idx_adivinanzas_creado_en ON adivinanzas_diarias (creado_en DESC)`);

    console.log('Tablas diarias creadas/verificadas correctamente.');
  } catch (error) {
    console.error('ERROR creando tablas diarias:', error.message);
    process.exitCode = 1;
  } finally {
    await pool.end();
  }
}

main();
